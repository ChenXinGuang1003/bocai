                          <td> <div align="left"> 您可将筹码下注到两行相交的最顶部的交点处 ， 您可以赌两个街注，也就是两行的六个数位。 (如28、29、30及31、32、33便组成一条Line)。 </div></td>
                        </tr>
                        <tr bgcolor="#DFFFFF">
                          <td bgcolor="#DFFFFF"><div align="center"> 列注 </div></td>
