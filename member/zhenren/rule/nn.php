                <td scope="col"><div align="left">
                        游戏使用扑克牌中52张牌（不含鬼牌），每局每家派发5张牌，先从5张牌中找出3张可成10或10的倍数组合，剩余2张牌相加，如遇点数相加超过10点则减去10点，为该局之点数，与庄家比点数大小决定胜负。<br>
                        <br>
                        你有以下几种下注选择：<br>
                        『闲一』：平倍、翻倍。<br>
                        『闲二』：平倍、翻倍。<br>
                        『闲三』：平倍、翻倍。<br>
                        <br>
                        <font color="red">注意：于翻倍中下注时，『可用余额』必须高于下注额度的5倍（下注分数 X 5公赔率），详情请看『其他补充说明』</font><br>
                        <br>
                        ●<b>头牌：</b>发牌员每局开始会先开「头牌」，依据头牌点数(从庄家方位逆时针方向算起)决定开牌位置。<br>
                        <img width="560" height="177" src="images/pic01_cn.jpg"><br><br>
                        ●<b>开牌顺序：</b>以逆时针方向开牌 (如下)
