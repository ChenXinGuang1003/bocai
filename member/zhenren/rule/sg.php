                            <li><p>使用1副扑克牌，下注结束后开始发牌，发牌时先开头牌决定发牌顺序，再以逆时钟方向进行，每门连续发3张牌。 </p></li>
                            <li><p>游戏主旨为闲家与庄家比较牌的 点数高低来决定胜负。 </p></li>
                            <li>
                                <p>会员可以有以下的下注选择 ：  </p>
                                <p>闲 1 ： 赢、输、和、三公、对牌以上 </p>
                                <p>闲 2 ： 赢、输、和、三公、对牌以上 </p>
                                <p>闲 3 ： 赢、输、和、三公、对牌以上 </p>
                                <p>庄家 ： 对牌以上 </p>
                                <p><span style="color: red;">注 ：各下注以及对牌以上赔率请见派彩说明。</span></p>
                            </li>
