                <p class="b sub">
                    游戏玩法</p>
                <h1>
                    <strong>1、选号玩法</strong></h1>
                <p>
                    <strong>单选：</strong>于百、十、个任选一位(百、十或个)、二位(百十、百个或十个)或三位(百十个)，分别自0~9选择1个号码、2个号码或3个号码，只要当期开奖结果与所选的号码相同且顺序一致时，即为中奖。
                    <br>
                    <span class="point">※举例：</span>投注者购买单选百位，选择号码为1，当期开奖结果如果只要百位数为1，十位及个位数无论为1xx皆视为中奖。（x=0~9任一数）
                    <br>
                    <span class="point">※举例：</span>投注者购买单选百十定位，选择号码为百位1、十位2，当期开奖结果如果只要百位与十位皆与其所选的号码相同且顺序一致时，个位数无论为12x皆视为中奖。（x=0~9任一数）
                    <br>
                    <span class="point">※举例：</span>投注者购买单选百十个定位，选择号码为百位1、十位2、个位3，当期开奖结果如为123，即视为中奖。
                    <br>
                    <strong>组一：</strong>自0~9任选1号进行投注，当开奖结果百十个任一数与所选的号码相同时，即为中奖；若开奖结果出现重覆数字时，视为中奖一次。
                </p>
                <span class="point">※举例：</span>投注者购买一字组合，选择号码为1，当期开奖结果如为1xx、x1x、xx1皆视为中奖。（x=0~9任一数）；若开奖的结果为11x、1x1、x11或111仅视为中奖一次
                <p>
                    <strong>组二：</strong>自0~9任选2号进行投注，当开奖结果百十个任二数与所选的号码相同时，即为中奖。
                    <br>
                    <span class="point">※举例：</span>投注者购买二字组合，选择号码为12，当期开奖结果如为12x、1x2、21x、2x1、x12、x21皆视为中奖。（x=0~9任一数）
                    <br>
                    <strong>组三：</strong>自0~9号任选3号且其中2个号需相同（如112），当开奖结果与所选号码相同(顺序不一定要相同)时，即为中奖。
                </p>
                <span class="point">※举例：</span>投注者购买组三，选择号码为112，当期开奖结果如为112、121、211皆视为中奖。
                <br>
                <strong>组六：</strong>自0~9号任选3号且3个号都不相同时（如123），当开奖结果与所选号码相同(顺序不一定要相同)时，即为中奖。
                <p>
                    <span class="point">※举例：</span>投注者购买组六，选择号码为123，当期开奖结果如为123、132、213、231、312、321皆视为中奖。<br>
                </p>
                <br>
                <h1>
                    <strong>2、和值玩法</strong></h1>
                以中奖号码个位、十位、百位数字相加起来的总和值为投注中奖的标准。<br><br>
                <h1>
                    <strong>3、佰、十、个 单双大小</strong></h1>
                中奖号码个位、十位、百位的单双大小<br>
                <strong>单双玩法:</strong>0、2、4、6、8 为双；1、3、5、7、9 为单
                <br>
                <strong>大小玩法:</strong>0、1、2、3、4 为小；5、6、7、8、9 为大
                <br>
                <strong>过串玩法:</strong>个十百单双大小串—在个位、十位、百位中任选一位、两位或者三位进行单双大小投注。
                <br><br>
                <h1>
                    <strong>4、跨度</strong></h1>
                以开奖三个号码的最大差距(跨度)，作为中奖的依据。会员可以选择 0 ~ 9 的任一跨度。
                <p>
                    <span class="point">※举例：</span>开奖结果为 3，4，8。中奖的跨度为 5。(最大号码 8 减 最小号码 3 = 5)。投注者购买跨度，选择号码为5，视为中奖。</p><br>
