                <p class="b sub"><strong>广东11选5游戏规则</strong><br />
                  <br />
                  该游戏的投注时间、开奖时间和开奖号码与广东11选5完全同步，北京时间（GMT+8）每天白天从上午9：00开到晚上23：00，每 10 分钟开一次奖，每天开奖 84 期。<br />
                  <br />
                  <strong>1、第一球 ~ 第五球</strong><br />
<br />
<strong>第一球~第五球：</strong><br />
第一球、第二球、第三球、第四球、第五球：指下注的每一球特码与开出之号码其开奖顺序及开奖号码相同，视为中奖，如第一球开出号码 8，下注第一球为 8 者视为中奖，其余情形视为不中奖。<br />
<strong>单双大小</strong><br />
根据相应单项投注第一球 ~ 第五球开出的球号，判断胜负。<br />
<strong>大小：</strong><br />
根据相应单项投注的第一球 ~ 第五球开出的球号大于或等于 6 为特码大，小于或等于 5 为特码小。<br />
<strong>单双：</strong><br />
根据相应单项投注的第一球 ~ 第五球开出的球号为双数则为特码双，如 2、6；球号为单数则为特码单，如 1、3。<br />
<strong>2、总和</strong><br />
<br />
<strong>大小：</strong><br />
根据相应单项投注的第一球 ~ 第五球开出的球号数字总和值大于或等于 31 为总和大，小于或等于 29 为总和小，等于30和局。<br />
<strong>单双：</strong><br />
根据相应单项投注的第一球 ~ 第五球开出的球号数字总和值是双数为总和双，数字总和值是单数为总和单。<br />
<strong>3、前三特殊玩法： 顺子 &gt; 半顺 &gt; 杂六</strong><br />
<br />
<strong>顺子：</strong><br />
开奖号码的万位千位百位数字都相连，不分顺序（数字9、0、1相连）。如中奖号码为：123XX、901XX、321XX、798XX...开奖号码的万位千位百位数字相连，则投注前三顺子者视为中奖，其它视为不中奖。<br />
<strong>半顺：</strong><br />
开奖号码的万位千位百位任意两位数字相连，不分顺序（不包括顺子、对子）。如中奖号码为：125XX、540XX、390XX、160XX...开奖号码的万位千位百位有两位数字相连，则投注前三半顺者视为中奖，其它视为不中奖。如果开奖号码为前三顺子、前三对子，则前三半顺视为不中奖。如开奖号码为：123XX、901XX、556XX、233XX...视为不中奖。<br />
<strong>杂六：</strong><br />
不包括豹子、对子、顺子、半顺的所有开奖号码。如开奖号码为：157XX、268XX...开奖号码位数之间无关联性，则投注前三杂六者视为中奖，其它视为不中奖。<br />
<strong>4、中三特殊玩法： 顺子 &gt; 半顺 &gt; 杂六</strong><br />
<br />
<strong>顺子：</strong><br />
开奖号码的千位百位十位数字都相连，不分顺序（数字9、0、1相连）。如中奖号码为：123XX、901XX、321XX、798XX...开奖号码的万位千位百位数字相连，则投注前三顺子者视为中奖，其它视为不中奖。<br />
<strong>半顺：</strong><br />
开奖号码的千位百位十位任意两位数字相连，不分顺序（不包括顺子、对子）。如中奖号码为：125XX、540XX、390XX、160XX...开奖号码的万位千位百位有两位数字相连，则投注前三半顺者视为中奖，其它视为不中奖。如果开奖号码为前三顺子、前三对子，则前三半顺视为不中奖。如开奖号码为：123XX、901XX、556XX、233XX...视为不中奖。<br />
<strong>杂六：</strong><br />
不包括豹子、对子、顺子、半顺的所有开奖号码。如开奖号码为：157XX、268XX...开奖号码位数之间无关联性，则投注前三杂六者视为中奖，其它视为不中奖。<br />
<strong>5、后三特殊玩法： 顺子 &gt; 半顺 &gt; 杂六<br />
<br />
顺子：</strong><br />
开奖号码的百位十位个位数字都相连，不分顺序（数字9、0、1相连）。如中奖号码为：123XX、901XX、321XX、798XX...开奖号码的万位千位百位数字相连，则投注前三顺子者视为中奖，其它视为不中奖。<br />
<strong>半顺：</strong><br />
开奖号码的百位十位个位任意两位数字相连，不分顺序（不包括顺子、对子）。如中奖号码为：125XX、540XX、390XX、160XX...开奖号码的万位千位百位有两位数字相连，则投注前三半顺者视为中奖，其它视为不中奖。如果开奖号码为前三顺子、前三对子，则前三半顺视为不中奖。如开奖号码为：123XX、901XX、556XX、233XX...视为不中奖。<br />
<strong>杂六：</strong><br />
不包括豹子、对子、顺子、半顺的所有开奖号码。如开奖号码为：157XX、268XX...开奖号码位数之间无关联性，则投注前三杂六者视为中奖，其它视为不中奖。<br />
<strong>6、龙虎和特殊玩法： 龙 &gt; 虎 &gt; 和 （0为最小，9为最大）</strong><br />
<br />
<strong>龙：</strong><br />
开奖第一球（万位）的号码 大于 第五球（个位）的号码。如：第一球开出 6，第五球开出 2、第一球开出 8，第五球开出 6、第一球开出 5，第五球开出 <strong>1...开奖为龙，投注龙者视为中奖，其它视为不中奖。</strong><br />
<strong>虎：</strong><br />
开奖第一球（万位）的号码 小于 第五球（个位）的号码。如：第一球开出 2，第五球开出 6、第一球开出 6，第五球开出 8、第一球开出 1，第五球开出 <strong>5...开奖为虎，投注虎者视为中奖，其它视为不中奖。</strong><br />
<strong>和：</strong><br />
开奖第一球（万位）的号码 等于 第五球（个位）的号码。如：2XXX2、6XXX6、8XXX8...开奖为和，投注和者视为中奖，其它视为不中奖。</p>
