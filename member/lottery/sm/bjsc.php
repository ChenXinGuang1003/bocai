                <p class="b sub">
                    游戏玩法</p>
                <h1>
                    <strong>1、第一名 ~ 第十名</strong></h1>
                <p>
                    <b><span class="point">※第一名~第十名：</span></b>车号指定，每一个车号为一投注组合，开奖结果"投注车号"对应所投名次视为中奖，其余情形视为不中奖。
                </p>
                <p>
                    <b><span class="point">※两面：</span></b>指单、双；大、小。</p>
                <p style="padding-left: 2em;">
                    单、双：号码为双数叫双，如8、10；号码为单数叫单，如9、5。</p>
                <p style="padding-left: 2em;">
                    大、小：开出之号码大于或等于6为大，小于或等于5为小。</p>
                <br />
                <h1>
                    <strong>2、1~5龙虎</strong></h1>
                <p>
                    <b><span class="point">※冠　军 龙/虎：</span></b>"第一名"车号大于"第十名"车号视为【龙】中奖、反之小于视为【虎】中奖，其余情形视为不中奖。</p>
                <p>
                    <b><span class="point">※亚　军 龙/虎：</span></b>"第二名"车号大于"第九名"车号视为【龙】中奖、反之小于视为【虎】中奖，其余情形视为不中奖。</p>
                <p>
                    <b><span class="point">※第三名 龙/虎：</span></b>"第三名"车号大于"第八名"车号视为【龙】中奖、反之小于视为【虎】中奖，其余情形视为不中奖。</p>
                <p>
                    <b><span class="point">※第四名 龙/虎：</span></b>"第四名"车号大于"第七名"车号视为【龙】中奖、反之小于视为【虎】中奖，其余情形视为不中奖。</p>
                <p>
                    <b><span class="point">※第五名 龙/虎：</span></b>"第五名"车号大于"第六名"车号视为【龙】中奖、反之小于视为【虎】中奖，其余情形视为不中奖。</p>
                <br />
                <h1>
                    <strong>3、冠亚军和</strong></h1>
                <p>
                    <b><span class="point">※冠军车号＋亚军车号＝冠亚和值：</span></b>可能出现的结果为3～19，投中对应"冠亚和值"数字的视为中奖，其余视为不中奖。</p>
                <p>
                    <b><span class="point">※冠亚和大小：</span></b>大于11时投注"大"的注单视为中奖，小于或等于11时投注"小"的注单视为中奖，其余视为不中。</p>
                <p>
                    <b><span class="point">※冠亚和单双：</span></b>为单视为投注"单"的注单视为中奖，为双视为投注"双"的注单视为中奖，其余视为不中奖。</p>
